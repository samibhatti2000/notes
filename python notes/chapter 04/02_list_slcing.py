friends = ["harry", "tom", "rohan", "Sam", "divya", "45"]
print("\n",friends[0:], "\n")
print("is same as [0:2]:",friends[:2]) #is same as [0:2]
print("is same as [0:4]:",friends[0:4]) #is same as [0:4]
print("#[-1] is same as [4] # negtive indexing:",friends[-1]) #is same as [5] # negtive indexing
print("is same as [:-2]:",friends[:-2]) #is same as [0:-2]

